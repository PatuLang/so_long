#include "../libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*cur;
	t_list	*prev;

	if (!*lst)
		return ;
	cur = *lst;
	while (cur != NULL)
	{
		prev = cur;
		cur = cur->next;
		del(prev->content);
		free(prev);
	}
	*lst = NULL;
}
